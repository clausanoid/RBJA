class Calculadora:
    def __init__(self, n1, n2):
        self.n1 = n1
        self.n2 = n2
        
    def sumar(self):
        print(str(self.n1 + self.n2))
    
    def restar(self):
        print(str(self.n1 - self.n2))
        
    def multiplicar(self):
        print(str(self.n1 * self.n2))
    
    def dividir(self):
        print(str(self.n1 / self.n2))
        
        
